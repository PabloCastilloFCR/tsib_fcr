def main():
    by_comuna = defaultdict(list)
    for region, provincia, comuna, zona, meridiano, altitud in ROWS:
        by_comuna[(region, provincia, comuna)].append(
            {"zona_termica": zona, "condicion_meridiano": meridiano, "condicion_altitud": altitud}
        )

    comunas = []
    for (region, provincia, comuna), reglas in by_comuna.items():
        comunas.append(
            {
                "region": region,
                "provincia": provincia,
                "comuna": comuna,
                "unico": len(reglas) == 1 and reglas[0]["condicion_meridiano"] is None and reglas[0]["condicion_altitud"] is None,
                "reglas": reglas,
            }
        )

    out = {
        "titulo": "Zonificacion termica por comuna",
        "fuente": "TABLA-ZT_REGIONES_PROVINCIAS-Y-COMUNAS.pdf (OGUC Art. 4.1.10 / Reglamentacion Termica, zonas A-I)",
        "nota": (
            "'unico'=true significa que la comuna tiene una unica zona termica sin "
            "condiciones (mayoria de los casos). Cuando 'unico'=false, 'reglas' "
            "lista 2-3 zonas posibles condicionadas a altitud (msnm) y/o meridiano "
            "(longitud); resolver requiere la altitud/longitud real del edificio, "
            "no solo el nombre de la comuna."
        ),
        "comunas": comunas,
    }

    with open(OUT_PATH, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)

    n_unico = sum(1 for c in comunas if c["unico"])
    print(f"Wrote {len(comunas)} comunas to {OUT_PATH}")
    print(f"  {n_unico} with a single unconditional zone, {len(comunas) - n_unico} with altitude/meridian conditions")


if __name__ == "__main__":
    main()
